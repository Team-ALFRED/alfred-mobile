package edu.utexas.seniordesign.alfred.models;

public class Location {
    private float x;
    private float y;

    public Location() {}

    public void setX(float v) {
        this.x = v;
    }

    public float getX() {
        return x;
    }

    public void setY(float v) {
        this.y = v;
    }

    public float getY() {
        return y;
    }
}

